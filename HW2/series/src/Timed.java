public class Timed {
    double time; // To store time taken by algorithm in milliseconds
    int num; // To store number of digits in result

    /**
     * Initializes the class.
     * @param t The time taken by the algorithm in milliseconds.
     * @param n The number of digits in the result.
     */
    Timed(double t, int n) {
        time = t;
        num = n;
    } 
}
